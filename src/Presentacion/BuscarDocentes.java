    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        modelo.setRowCount(0);
        String id = txtId.getText();

        try {
            PersistenciaDocente persistencia = new PersistenciaDocente();
            
            Docente docenteEncontrado = persistencia.buscarDocente(id); 

            modelo.addRow(new Object[]{
                docenteEncontrado.getId(), 
                docenteEncontrado.getNombre(), 
                docenteEncontrado.getAsignatura()
            });

        } catch (DocenteException de) {
            JOptionPane.showMessageDialog(this, de.getMessage(), "Docente No Encontrado", JOptionPane.WARNING_MESSAGE);
        } catch (BDException bdE) {
            JOptionPane.showMessageDialog(this, "Error de Conexi√≥n a BD: " + bdE.getMessage(), "Error Grave", JOptionPane.ERROR_MESSAGE);
        } catch (SQLException sqle) {
            JOptionPane.showMessageDialog(this, "Error de SQL: " + sqle.getMessage(), "Error SQL", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error inesperado: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

